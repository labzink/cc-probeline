func TestEmitVideoFrames(t *testing.T) {
	dir := os.Getenv("CC_PROBELINE_VIDEO_DIR")
	if dir == "" {
		t.Skip("set CC_PROBELINE_VIDEO_DIR to emit hero-video .ansi frames")
	}
	if !filepath.IsAbs(dir) {
		dir = filepath.Join(testutil.ProjectRoot(t), dir)
	}
	if err := os.MkdirAll(dir, 0o755); err != nil {
		t.Fatalf("mkdir %s: %v", dir, err)
	}

	th := realTheme()
	for _, vf := range videoFrames() {
		vf := vf
		t.Run(vf.name, func(t *testing.T) {
			d, cfg := videoData(t, vf)
			a := statusline.Assembler{Mode: mode.Standard, Theme: th, Cols: 130, Config: cfg}
			out := renderer.Apply(a.Render(d), th)
			path := filepath.Join(dir, vf.name+".ansi")
			if err := os.WriteFile(path, []byte(out), 0o644); err != nil {
				t.Fatalf("write %s: %v", path, err)
			}
			t.Logf("wrote %s (%d bytes)", path, len(out))
		})
	}
}

// videoData mirrors scenarioData (frames_test.go) but is data-driven by the
// vframe: per-beat fixture, subagent sidecar, observation moment, context window,
// quota and extra-usage. Opus 4.8, clean git, fixed 10-row window.
func videoData(t *testing.T, vf vframe) (probes.Data, probes.Config) {
	t.Helper()
	root := testutil.ProjectRoot(t)
	t.Setenv("CC_PROBELINE_QUOTA_DIR", t.TempDir())

	fixture := vf.fixture
	if fixture == "" {
		fixture = fxVideoAct1
	}
	records := parseTruncated(t, filepath.Join(root, fixture), vf.keepTurns)
	session := parser.Aggregate(parser.Dedup(records))

	var subagents []parser.SubagentStats
	if vf.subDir != "" {
		subs, err := parser.CollectSubagents(context.Background(), filepath.Join(root, vf.subDir))
		if err != nil {
			t.Fatalf("CollectSubagents: %v", err)
		}
		subagents = subs
	}

	st := &state.Session{}
	allTurns := make([]parser.Turn, len(session.Turns))
	copy(allTurns, session.Turns)
	for i := range subagents {
		allTurns = append(allTurns, subagents[i].Turns...)
	}
	durMS := vf.now.Sub(vt(8, 0)).Milliseconds()
	cost.Reconcile(st, 0, 0, nil)
	cost.Reconcile(st, 0, durMS, allTurns)
	// The header reads the official meter (d.Stdin.Cost.TotalCostUSD), not our
	// estimate (Phase 7.46, probes/cost.go). For the synthetic video we feed the
	// estimate Σ in as that "official" total so the header tracks the visible
	// turns instead of reading $0.00 from an absent live meter.
	sessTotal := cost.SessionTotal(st, 0)

	ctxSize := vf.ctxSize
	if ctxSize == 0 {
		ctxSize = 1_000_000
	}
	ctxUsed := vf.ctxUsed
	if ctxUsed == 0 {
		if n := len(session.Turns); n > 0 {
			ctxUsed = session.Turns[n-1].Tokens.CacheRead
		}
		if ctxUsed == 0 {
			ctxUsed = 60_000
		}
	}

	payload := stdin.Payload{
		Model:         opus48,
		SessionID:     "video-" + vf.name,
		Cwd:           "/Users/dev/Projects/cc-probeline",
		ContextWindow: ctxWindow(ctxSize, ctxUsed),
		Cost:          stdin.Cost{TotalCostUSD: sessTotal, TotalAPIDurationMS: durMS},
		RateLimits:    vrl(vf),
	}

	var events []parser.CacheEvent
	if vf.rebuilt {
		events = []parser.CacheEvent{{Type: parser.OrchTTL, Timestamp: vf.now}}
	}

	d := probes.Data{
		Stdin:            payload,
		Session:          &session,
		Subagents:        subagents,
		Git:              &parser.GitStatus{Branch: "main", ModifiedCount: 0},
		Now:              vf.now,
		SessionID:        payload.SessionID,
		State:            st,
		ExtraCacheEvents: events,
		ExtraActive:      vf.extraActive,
		ExtraUSD:         vf.extraUSD,
		HintStart:        vf.hintStart,
	}
	d.SessionTotal = sessTotal
	d.SessionDurMS = cost.SessionDuration(st, durMS)
	curGroupID := 0
	if len(session.Turns) > 0 {
		curGroupID = session.Turns[len(session.Turns)-1].GroupID
	}
	d.LastRequestCost = cost.LastRequest(st, 0, curGroupID)
	captured := st
	d.PerTurnCostFn = func(uuid string) (float64, bool) { return cost.PerTurn(captured, uuid) }

	cfg := richProbesCfg()
	cfg.TableRows = 10 // fixed sliding window — turns appear one at a time
	return d, cfg
}

// vrl builds the per-beat RateLimits, anchoring the reset countdown to the frame's
// own `now` so the "↻ time-to-reset" reads sensibly across the moving timeline.
func vrl(vf vframe) *stdin.RateLimits {
	return &stdin.RateLimits{
		FiveHour: stdin.RateWindow{UsedPercentage: vf.pct5h, ResetsAt: jsonTime(vf.now.Add(vf.reset5h))},
		SevenDay: stdin.RateWindow{UsedPercentage: vf.pct7d, ResetsAt: jsonTime(vf.now.Add(vf.reset7d))},
	}
}

// parseTruncated reads the fixture and keeps records up to and including the
// keepTurns-th ASSISTANT record (plus any user records before it), synthesising
// an earlier point in the session where the newest turn is #keepTurns.
func parseTruncated(t *testing.T, path string, keepTurns int) []parser.Record {
	t.Helper()
	raw, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read %s: %v", path, err)
	}
	var kept []string
	assistants := 0
	for _, line := range strings.Split(string(raw), "\n") {
		if strings.TrimSpace(line) == "" {
			continue
		}
		kept = append(kept, line)
		if strings.Contains(line, `"type": "assistant"`) || strings.Contains(line, `"type":"assistant"`) {
			assistants++
			if assistants >= keepTurns {
				break
			}
		}
	}
	records, _, scanErr := parser.ParseLines(strings.NewReader(strings.Join(kept, "\n") + "\n"))
	if scanErr != nil {
		t.Fatalf("ParseLines truncated %s: %v", path, scanErr)
	}
	return records
}
