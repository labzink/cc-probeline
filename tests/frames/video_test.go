// vframe is one storyboard beat. The fixture is truncated to its first keepTurns
// ASSISTANT turns (so the newest visible turn is #keepTurns), observed at moment
// `now`. A fixed 10-row window (videoData sets TableRows=10) makes turns appear
// one at a time. Quota / context / extra-usage are set per-beat to drive the
// Act-2 escalation; hintStart rotates the bottom tip every few beats.
type vframe struct {
	name      string
	fixture   string // default fxVideoAct1
	subDir    string // sidecar dir → collect a subagent ↳ row (Act 1)
	keepTurns int    // newest visible assistant turn number
	now       time.Time

	ctxSize int // context-window size; 0 → 1_000_000
	ctxUsed int // 0 → newest turn's cache_read

	pct5h, pct7d     float64
	reset5h, reset7d time.Duration
	extraActive      bool
	extraUSD         float64
	rebuilt          bool // emit the "⚠ Cache rebuilt" alert (overrides the hint)
	hintStart        int
}

// vt builds an observation moment on the fixture's day (2026-06-05 UTC).
func vt(h, m int) time.Time { return time.Date(2026, 6, 5, h, m, 0, 0, time.UTC) }

