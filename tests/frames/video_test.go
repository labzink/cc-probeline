// video_test.go — Phase 7.47 hero-video frame emitter.
//
// Sibling of frames_test.go (the static README frames). This file owns the
// VIDEO storyboard: a sequence of .ansi frames that, played back in order with
// timing (assets/video/hero.tape), animates the master-fable-rebuild ramp —
// cost dripping, cache_read climbing, the ⏱ TTL ageing, and finally the 240K
// cache rebuild flashing red on the freshest turn.
//
// Same honesty principle as the static frames: states are synthetic (the fixture
// is truncated to a growing turn count and observed at a moving `now`), but the
// render is the REAL production pipeline (assembler → renderer.Apply). Each frame
// is internally honest — per-turn $ is each turn's real token price (Phase 7.46
// table-driven cost), the session total is the sum of the visible turns.
//
// Gated behind CC_PROBELINE_VIDEO_DIR (separate from the static emitter's
// CC_PROBELINE_EMIT_DIR) so `go test ./...` stays hermetic and asset-free:
//
//	CC_PROBELINE_VIDEO_DIR=<dir> go test ./tests/frames/ -run EmitVideoFrames -v
//
